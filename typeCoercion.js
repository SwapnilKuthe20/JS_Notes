// #	Type coercion ?
// Answer: Type coercion means automatically converting one data type to another when necessary 
//          if the data types don't match.
	
// 	Types of Type Coercion:
// 		1. Implicit Coercion (Automatic) : JavaScript automatically converts data types when needed.
 
// 		2. Explicit Coercion (Manual) : Manually converting data types using JavaScript methods.
//           using String(), Number(), Boolean() etc,.

// 1. Implicit Coercion (Automatic) :

// String Coercion
// When a number or boolean is added to a string, it is converted to a string.

